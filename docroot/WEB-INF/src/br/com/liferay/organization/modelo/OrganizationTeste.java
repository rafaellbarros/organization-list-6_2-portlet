package br.com.liferay.organization.modelo;

public class OrganizationTeste {

	private String nome;
	private String sites;
	private String localidade;
	private String usuarios;
	private String suborganizacao;

	
	
	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getSites() {
		return sites;
	}

	public void setSites(String sites) {
		this.sites = sites;
	}

	public String getLocalidade() {
		return localidade;
	}

	public void setLocalidade(String localidade) {
		this.localidade = localidade;
	}

	public String getUsuarios() {
		return usuarios;
	}

	public void setUsuarios(String usuarios) {
		this.usuarios = usuarios;
	}

	public String getSuborganizacao() {
		return suborganizacao;
	}

	public void setSuborganizacao(String suborganizacao) {
		this.suborganizacao = suborganizacao;
	}

}
